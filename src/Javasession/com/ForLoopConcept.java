		System.out.println("Printing the values from 1 to 10 using Forloop");
		
		
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
			
		}
		
System.out.println("Printing the even values from 1 to 10 using Forloop");
		
		
		
		for(int i=2;i<=10;i=i+2)
		{
			System.out.println(i);
			
		}	
		
		
System.out.println("Printing the odd values from 1 to 10 using Forloop");
		
		
		
		for(int i=1;i<=10;i=i+2)
		{
			System.out.println(i);
			
		}		
		
		
		//Infinite for loop
		
		
