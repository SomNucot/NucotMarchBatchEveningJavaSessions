package Javasession.com;

public class IncrementandDecrementOperatorDemo {

	public static void main(String[] args) {
		
		//++ and --
		//Post increment-->example-->a++
		//Pre increment--->++a
		//Post decrement-->a--
		//pre decrement-->--a
		
		int a=1;
		int b=a++;//post increment
		System.out.println(a);//2
		System.out.println(b);//1
		
		int aa=3;
		int bb=aa++;//post increment
		System.out.println(aa);//4
		System.out.println(bb);//3
		
		
		int aaa=1;
		int bbb=++aaa;//pre increment
		System.out.println(aaa);//2
		System.out.println(bbb);//2
		
		
		int aaaa=3;
		int bbbb=++aaaa;//post increment
		System.out.println(aaaa);//
		System.out.println(bbbb);//
		
		//Post decrement-->a--
		
		
		int aaaaa=3;
		int bbbbb=aaaaa--;//post increment
		System.out.println(aaaaa);//2
		System.out.println(bbbbb);//3
		
		
		int p=-1;
		int q=p--;//post increment
		System.out.println(p);//-2
		System.out.println(q);//-1
		
		//Pre decrement-->--a
		int aaaaaa=3;
		int bbbbbb=--aaaaaa;//post increment
		System.out.println(aaaaaa);//2
		System.out.println(bbbbbb);//2
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
